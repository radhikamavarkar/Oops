package com.xworkz.Polymorphism.Multipleinheritance;

public  class Cat implements Animal { 
	public void sleep() { 
		System.out.println("cat is always sleeping");
	} 
	public void run() { 
		System.out.println("cat is run"); 
	} 
	public void sound() { 
		System.out.println("cat make sound");
	}

}
