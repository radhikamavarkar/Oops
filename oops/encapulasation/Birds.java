package com.xworkz.oops.encapulasation;

public class Birds { 
	private String feathers; 
	private int wings; 
	private int beaks; 
	private int weight; 
	private int beak; 
	private String colour; 
	private int numleg; 
	private String type; 
	private String body;
	
	public Birds() { 
		
	}  
	 
	
	public String getFeathers() {
		return feathers;
	}
	public void setFeathers(String feathers) {
		this.feathers = feathers;
	}
	public int getWings() {
		return wings;
	}
	public void setWings(int wings) {
		this.wings = wings;
	}
	public int getBeaks() {
		return beaks;
	}
	public void setBeaks(int beaks) {
		this.beaks = beaks;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	public int getBeak() {
		return beak;
	}
	public void setBeak(int beak) {
		this.beak = beak;
	}
	public String getColour() {
		return colour;
	}
	public void setColour(String colour) {
		this.colour = colour;
	}
	public int getNumleg() {
		return numleg;
	}
	public void setNumleg(int numleg) {
		this.numleg = numleg;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getBody() {
		return body;
	}
	public void setBody(String body) {
		this.body = body;
	}
	 
	
	

}
